package common


func ToChaincodeArgs(args ...string) []string {
	bargs := make([]string, len(args))
	for i, arg := range args {
		bargs[i] = arg
	}
	return bargs
}

func ArrayToChaincodeArgs(args []string) [][]byte {
	bargs := make([][]byte, len(args))
	for i, arg := range args {
		bargs[i] = []byte(arg)
	}
	return bargs
}
